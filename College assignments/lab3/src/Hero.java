public class Hero {
    private String name;
    private String classe;
    private String equipamento1;
    private String equipamento2;
    private int strength;
    private int speed;
    private int inteligence;
    private int senses;
    private int dexterity;
    private int adaptability;
    private int health;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getClasse() {
        return classe;
    }

    public void setClasse(String classe) {
        this.classe = classe;
    }

    public String getEquipamento1() {
        return equipamento1;
    }

    public void setEquipamento1(String equipamento1) {
        this.equipamento1 = equipamento1;
    }

    public String getEquipamento2() {
        return equipamento2;
    }

    public void setEquipamento2(String equipamento2) {
        this.equipamento2 = equipamento2;
    }

    public int getStrength() {
        return strength;
    }

    public void setStrength(int strength) {
        this.strength = strength;
    }

    public int getSpeed() {
        return speed;
    }

    public void setSpeed(int speed) {
        this.speed = speed;
    }

    public int getInteligence() {
        return inteligence;
    }

    public void setInteligence(int inteligence) {
        this.inteligence = inteligence;
    }

    public int getSenses() {
        return senses;
    }

    public void setSenses(int senses) {
        this.senses = senses;
    }

    public int getDexterity() {
        return dexterity;
    }

    public void setDexterity(int dexterity) {
        this.dexterity = dexterity;
    }

    public int getAdaptability() {
        return adaptability;
    }

    public void setAdaptability(int adaptability) {
        this.adaptability = adaptability;
    }

    public int getHealth() {
        return health;
    }

    public void setHealth(int health) {
        this.health = health;
    }
}
